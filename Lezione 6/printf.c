#include <stdio.h>

int main()
{
    printf("Ciao\nCiao");
    printf("\n\n\n");
    
    /* 123 vengono sovrascritti */
    printf("12345\rabc\n");
    
    printf("\n\n");
    printf("C:\\Users\\User\n");

    printf("\n\n");
    printf("Ciao\tQualcosa\tqualcosaltro\n");
    printf("Csiao\tlcosa\tlcosaltro");
    /* equivale a TAB */
    /* gli \t possono anche essere concatenati*/
    
    printf("\n\n");
    printf("con questa frase voglio dire \"qualcosa\"\n");
    
    printf("\n\n");
    printf("con questa frase voglio dire \'qualcosa\'\n");
    
    printf("\a");
    printf("\n\n\n\n");
    
    printf("%%\n");
    
    
}
